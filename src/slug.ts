/**
 * Преобразует заголовок задачи в URL-slug.
 *
 * Алгоритм:
 *  1. Привести к нижнему регистру.
 *  2. Убрать ведущие/замыкающие пробелы.
 *  3. Заменить любые последовательности не-буквенно-цифровых символов на один дефис.
 *  4. Убрать ведущие/замыкающие дефисы.
 *  5. Строка без букв/цифр → "".
 *
 * Примеры:
 *   slugify("Buy Milk!")           → "buy-milk"
 *   slugify("  Hello,  World  ")   → "hello-world"
 *   slugify("!@#$")                → ""
 */
export function slugify(title: string): string {
  // Шаг 1 + 2: нижний регистр и обрезка пробелов по краям.
  const trimmed = title.trim().toLowerCase();

  // Шаг 3: заменяем любые непрерывные не-буквенно-цифровые символы на один дефис.
  const hyphenated = trimmed.replace(/[^a-z0-9]+/g, '-');

  // Шаг 4: убираем ведущие и замыкающие дефисы.
  return hyphenated.replace(/^-+|-+$/g, '');
}
