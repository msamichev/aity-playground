## Как этот файл регенерируется

При изменении `AGENTS.md` запустить:

```bash
bash scripts/gen-copilot-instructions.sh
```

Проверить актуальность (для CI):

```bash
bash scripts/gen-copilot-instructions.sh --check
```

`--check` возвращает exit 1, если `.github/copilot-instructions.md` расходится с тем, что сейчас сгенерировал бы скрипт.
"""

if mode == "--check":
    if not output.exists():
        print(f"✗ {output} не существует. Запусти: bash scripts/gen-copilot-instructions.sh")
        sys.exit(1)
    existing = output.read_text(encoding="utf-8")
    if existing.strip() != generated.strip():
        print(f"✗ {output} расходится с AGENTS.md. Запусти: bash scripts/gen-copilot-instructions.sh")
        sys.exit(1)
    print(f"✓ {output} в актуальном состоянии.")
    sys.exit(0)

# generate mode
output.parent.mkdir(parents=True, exist_ok=True)
output.write_text(generated, encoding="utf-8")
print(f"✓ Сгенерирован {output} ({len(generated)} символов).")
